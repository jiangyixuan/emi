package com.jyx.emi.bean;

public class PostsAcInfo {
	private int id;
	private String floor;
	private String oneuser;
	private String twouser;
	private String name;
	private int sid;
	private  String body;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getFloor() {
		return floor;
	}
	public void setFloor(String floor) {
		this.floor = floor;
	}
	public String getOneuser() {
		return oneuser;
	}
	public void setOneuser(String oneuser) {
		this.oneuser = oneuser;
	}
	public String getTwouser() {
		return twouser;
	}
	public void setTwouser(String twouser) {
		this.twouser = twouser;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getSid() {
		return sid;
	}
	public void setSid(int sid) {
		this.sid = sid;
	}

	public String getBody() {
		return body;
	}

	public void setBody(String body) {
		this.body = body;
	}
}
